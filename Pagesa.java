public class Pagesa {

    private int id;
    private double cmimi;

    Pagesa(int id, double cmimi) {
        id = this.id;
        cmimi = this.cmimi;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public double getCmimi() {
        return cmimi;
    }

    public void setCmimi(double cmimi) {
        this.cmimi = cmimi;
    }

}
